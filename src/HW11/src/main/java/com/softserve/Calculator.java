package com.softserve;

public class Calculator {
    public int getSum(int x, int y) {
        return x + y;
    }

    public int getDifference(int x, int y){
        return  x - y;
    }
    public int getDiv(int x, int y) {
        return x / y;
    }

    public int getMultiple(int x, int y){
        return x * y;
    }
}
